#define _USE_MATH_DEFINES
#include <iostream>
#include <cmath>
#include <cstdlib>
#include <conio.h>

using namespace std;

int main()
{
    double u, x, y, w, v, f, z;

    cout << "x = "; cin >> x;
    cout << "y = "; cin >> y;
    cout << "z = "; cin >> z;

    f = (-1) * exp(x * (sqrt(fabs(y)))) + z;

    v = sqrt(fabs((sin(cos(f)), 2) + x)), 3;

    w = f + v + log(fabs(x * y * z));

    u = x + y + exp((-1) * (w * v * f));

    cout << "U = " << u << endl;

    return 0;
}
